  name: "BasicFile",

  props: [
    "divCls",
    "cls",
    "placeholder",
    "val",
    "readonly",
    "disabled",
    "type"
  ],

  methods: {
  	bind: function (value) {
  	  this.$emit('input', value)
  	}
  },
};
